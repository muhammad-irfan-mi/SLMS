const updateStudentDefaulterStatus = async (studentId) => {
  const now = new Date();

  // Find all fees for this student that have remaining amount > 0 and due date passed
  const overdueFees = await FeeDetail.find({
    studentId,
    dueDate: { $lt: now },
    remainingAmount: { $gt: 0 }
  }).select('remainingAmount month finalAmount');

  const overdueCount = overdueFees.length;
  const overdueAmount = overdueFees.reduce((sum, fee) => sum + fee.remainingAmount, 0);
  const overdueMonths = overdueFees.map(fee => fee.month);

  // Count total pending fees (not fully paid)
  const totalPendingFees = await FeeDetail.find({
    studentId,
    remainingAmount: { $gt: 0 },
    status: { $ne: "rejected" }
  }).select('remainingAmount');

  const totalPendingCount = totalPendingFees.length;
  const totalPendingAmount = totalPendingFees.reduce((sum, fee) => sum + fee.remainingAmount, 0);

  const isDefaulter = overdueCount > 0;

  await Student.findByIdAndUpdate(studentId, {
    isDefaulter: isDefaulter
  });

  return {
    overdueCount,
    overdueAmount,
    totalPendingCount,
    totalPendingAmount,
    isDefaulter,
    overdueMonths
  };
};

// Helper to calculate student fee with discount snapshot
const calculateStudentFee = async (studentId, schoolId, customAmount = null) => {
  try {
    const student = await Student.findById(studentId)
      .select('classInfo discount isFixed school');

    if (!student) {
      throw new Error('Student not found');
    }

    if (customAmount !== null && customAmount !== undefined) {
      return {
        originalAmount: customAmount,
        discountAmount: 0,
        finalAmount: customAmount,
        discountType: null,
        discountValue: 0,
        source: 'custom'
      };
    }

    const classInfo = await ClassSection.findById(student.classInfo?.id)
      .select('fee');

    if (!classInfo) {
      throw new Error('Class not found for this student');
    }

    const classFee = classInfo.fee || 0;
    let finalAmount = classFee;
    let discountAmount = 0;
    let discountType = null;
    let discountValue = 0;

    if (student.discount > 0) {
      if (student.isFixed === true) {
        discountType = "fixed";
        discountValue = student.discount;
        discountAmount = Math.min(student.discount, classFee - 1);
        finalAmount = classFee - discountAmount;
      } else {
        discountType = "percentage";
        discountValue = student.discount;
        discountAmount = (classFee * student.discount) / 100;
        finalAmount = classFee - discountAmount;
      }
    } else {
      finalAmount = classFee;
    }

    return {
      originalAmount: classFee,
      discountAmount: discountAmount,
      finalAmount: finalAmount,
      discountType: discountType,
      discountValue: discountValue,
      source: 'class_fee'
    };
  } catch (error) {
    throw error;
  }
};

// Bulk Create Fee Details
const bulkCreateFeeDetails = async (req, res) => {
  try {
    const { error } = bulkCreateFeeDetailsSchema.validate(req.body);
    if (error) return res.status(400).json({ message: error.details[0].message });

    const { feeDetails } = req.body;
    const schoolId = req.user.school;
    const actor = req.user;

    const studentIds = feeDetails.map(fd => fd.studentId);
    const students = await Student.find({
      _id: { $in: studentIds },
      school: schoolId,
      isActive: true
    }).select('_id name email rollNo classInfo sectionInfo discount isFixed');

    const studentMap = new Map();
    students.forEach(student => {
      studentMap.set(student._id.toString(), student);
    });

    const invalidStudents = studentIds.filter(id => !studentMap.has(id));
    if (invalidStudents.length > 0) {
      return res.status(400).json({
        message: `Some students not found in your school`,
        invalidStudentIds: invalidStudents
      });
    }

    const images = req.files?.voucherImages || [];
    const imageMap = new Map();

    if (images.length > 0) {
      const uploadPromises = images.map(async (file) => {
        return await uploadFileToS3({
          fileBuffer: file.buffer,
          fileName: file.originalname,
          mimeType: file.mimetype,
        });
      });
      const uploadedImageUrls = await Promise.all(uploadPromises);
      images.forEach((image, index) => {
        if (index < feeDetails.length) {
          imageMap.set(index, uploadedImageUrls[index]);
        }
      });
    }

    const feeDocuments = await Promise.all(feeDetails.map(async (feeData, index) => {
      const student = studentMap.get(feeData.studentId);
      let feeCalculation;

      if (feeData.amount) {
        feeCalculation = await calculateStudentFee(feeData.studentId, schoolId, feeData.amount);
      } else {
        feeCalculation = await calculateStudentFee(feeData.studentId, schoolId);
      }

      const feeDoc = {
        studentId: feeData.studentId,
        school: schoolId,
        month: feeData.month,
        dueDate: new Date(feeData.dueDate),
        originalAmount: feeCalculation.originalAmount,
        discountAmount: feeCalculation.discountAmount,
        finalAmount: feeCalculation.finalAmount,
        paidAmount: 0,
        remainingAmount: feeCalculation.finalAmount,
        title: feeData.title,
        description: feeData.description || '',
        discountType: feeCalculation.discountType,
        discountValue: feeCalculation.discountValue,
        status: "pending"
      };

      if (imageMap.has(index)) {
        feeDoc.voucherImage = imageMap.get(index);
      }

      return feeDoc;
    }));

    const createdFees = await FeeDetail.insertMany(feeDocuments);
    await Promise.all(createdFees.map(fee => updateStudentDefaulterStatus(fee.studentId)));

    const notificationPromises = createdFees.map(async (fee) => {
      try {
        await sendFeeNotificationToStudent(fee, actor, 'created');
      } catch (error) {
        console.error('Error sending notification for fee:', fee._id, error);
      }
    });
    await Promise.allSettled(notificationPromises);

    const populatedFees = await FeeDetail.find({
      _id: { $in: createdFees.map(f => f._id) }
    }).populate("studentId", "name email rollNo classInfo sectionInfo discount").lean();

    const formattedFees = await Promise.all(populatedFees.map(async (fee) => {
      const feeObj = { ...fee };
      let studentSchoolId = feeObj.studentId?.school || null;

      if (feeObj.studentId?.classInfo?.id) {
        const classSectionInfo = await getClassSectionInfo(
          feeObj.studentId.classInfo.id,
          feeObj.studentId.sectionInfo?.id || null,
          studentSchoolId
        );
        if (classSectionInfo.class) {
          feeObj.classInfo = { id: classSectionInfo.class._id, name: classSectionInfo.class.name };
        }
        if (classSectionInfo.section) {
          feeObj.sectionInfo = { id: classSectionInfo.section._id, name: classSectionInfo.section.name };
        }
      }

      if (feeObj.studentId) {
        feeObj.studentInfo = {
          _id: feeObj.studentId._id,
          name: feeObj.studentId.name,
          email: feeObj.studentId.email,
          rollNo: feeObj.studentId.rollNo,
          discount: feeObj.studentId.discount,
          school: feeObj.studentId.school
        };
        delete feeObj.studentId;
      }

      return feeObj;
    }));

    res.status(201).json({
      message: `Successfully created ${createdFees.length} fee details`,
      total: createdFees.length,
      fees: formattedFees,
    });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

