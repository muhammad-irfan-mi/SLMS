async function uploadImage(files, fieldName, existingImage = null) {
  let image = existingImage;

  if (files?.[fieldName]?.[0]) {
    if (image) await deleteFileFromS3(image);
    image = await uploadFileToS3({
      fileBuffer: files[fieldName][0].buffer,
      fileName: files[fieldName][0].originalname,
      mimeType: files[fieldName][0].mimetype,
    });
  }

  return image;
}

const getClassSectionInfo = async (classId, sectionId, schoolId) => {
  const classSection = await ClassSection.findOne({
    _id: classId,
    school: schoolId
  }).lean();

  if (!classSection) {
    return { class: null, section: null };
  }

  const classInfo = {
    _id: classSection._id,
    name: classSection.class
  };

  let sectionInfo = null;
  if (sectionId && classSection.sections) {
    const section = classSection.sections.find(
      sec => sec._id.toString() === sectionId.toString()
    );
    if (section) {
      sectionInfo = {
        _id: section._id,
        name: section.name
      };
    }
  }

  return { class: classInfo, section: sectionInfo };
};

// Notification functions
const sendFeeNotificationToStudent = async (fee, actor, action = 'created') => {
  try {
    const student = await Student.findById(fee.studentId).select('name email school');
    if (!student) return null;

    let title, message;
    if (action === 'created') {
      title = `${fee.month || ''} Fee Generated`;
      message = `A new fee of ${fee.finalAmount} for ${fee.month} has been generated by ${actor.name}.`;
    } else if (action === 'updated') {
      title = `${fee.month || ''} Fee Updated`;
      message = `Your fee for ${fee.month} has been updated by ${actor.name}. New amount: ${fee.finalAmount}`;
    } else if (action === 'payment_approved') {
      title = `Payment Approved`;
      message = `Your payment of ${fee.paidAmount} for ${fee.month} has been approved. Remaining: ${fee.remainingAmount}`;
    }

    return createNotification({
      type: NOTIFICATION_TYPES.FEE,
      actor,
      targetStudents: [fee.studentId],
      school: fee.school,
      title,
      message,
      data: {
        feeId: fee._id,
        finalAmount: fee.finalAmount,
        paidAmount: fee.paidAmount,
        remainingAmount: fee.remainingAmount,
        month: fee.month,
        title: fee.title,
        status: fee.status,
        action
      },
      category: 'general',
      pinned: false
    });
  } catch (error) {
    console.error('Error sending fee notification to student:', error);
    return null;
  }
};

