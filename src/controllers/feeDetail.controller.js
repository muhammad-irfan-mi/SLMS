// Get defaulter students
const getDefaulterStudents = async (req, res) => {
  try {
    const schoolId = req.user.school;
    const now = new Date();

    let { page = 1, limit = 10, month, classId, sectionId, search } = req.query;
    page = parseInt(page);
    limit = parseInt(limit);
    const skip = (page - 1) * limit;

    // Find all fees that are overdue (remaining > 0 and due date passed)
    const overdueFilter = {
      school: schoolId,
      dueDate: { $lt: now },
      remainingAmount: { $gt: 0 },
    };

    if (month) overdueFilter.month = month;

    const overdueFeesAggregation = [
      { $match: overdueFilter },
      {
        $lookup: {
          from: "students",
          localField: "studentId",
          foreignField: "_id",
          as: "student"
        }
      },
      { $unwind: "$student" },
      ...(classId ? [{ $match: { "student.classInfo.id": new mongoose.Types.ObjectId(classId) } }] : []),
      ...(sectionId ? [{ $match: { "student.sectionInfo.id": new mongoose.Types.ObjectId(sectionId) } }] : []),
      ...(search ? [{
        $match: {
          $or: [
            { "student.name": { $regex: search, $options: "i" } },
            { "student.rollNo": { $regex: search, $options: "i" } }
          ]
        }
      }] : []),
      {
        $group: {
          _id: "$student._id",
          student: { $first: "$student" },
          overdueFees: {
            $push: {
              _id: "$_id",
              month: "$month",
              remainingAmount: "$remainingAmount",
              originalAmount: "$originalAmount",
              finalAmount: "$finalAmount",
              dueDate: "$dueDate",
              title: "$title",
              daysOverdue: {
                $ceil: {
                  $divide: [
                    { $subtract: [now, "$dueDate"] },
                    1000 * 60 * 60 * 24
                  ]
                }
              }
            }
          },
          totalRemaining: { $sum: "$remainingAmount" },
          overdueCount: { $sum: 1 }
        }
      },
      { $sort: { totalRemaining: -1 } },
      {
        $facet: {
          data: [{ $skip: skip }, { $limit: limit }],
          totalCount: [{ $count: "count" }]
        }
      }
    ];

    const result = await FeeDetail.aggregate(overdueFeesAggregation);
    let data = result[0]?.data || [];
    const total = result[0]?.totalCount[0]?.count || 0;

    // Get total pending amounts for each student
    const allPendingFees = await FeeDetail.aggregate([
      {
        $match: {
          school: schoolId,
          remainingAmount: { $gt: 0 },
          status: { $nin: ["paid", "rejected"] }
        }
      },
      {
        $group: {
          _id: "$studentId",
          totalPendingAmount: { $sum: "$remainingAmount" },
          totalPendingCount: { $sum: 1 }
        }
      }
    ]);

    const pendingMap = new Map();
    allPendingFees.forEach(item => {
      pendingMap.set(item._id.toString(), {
        totalPendingAmount: item.totalPendingAmount,
        totalPendingCount: item.totalPendingCount
      });
    });

    data = await Promise.all(data.map(async (item) => {
      const student = item.student;
      const pendingInfo = pendingMap.get(student._id.toString()) || { totalPendingAmount: 0, totalPendingCount: 0 };

      let formattedClassInfo = null;
      let formattedSectionInfo = null;

      if (student?.classInfo?.id) {
        const classSectionInfo = await getClassSectionInfo(
          student.classInfo.id,
          student.sectionInfo?.id || null,
          student.school
        );
        if (classSectionInfo.class) {
          formattedClassInfo = { id: classSectionInfo.class._id, name: classSectionInfo.class.name };
        }
        if (classSectionInfo.section) {
          formattedSectionInfo = { id: classSectionInfo.section._id, name: classSectionInfo.section.name };
        }
      }

      return {
        _id: item._id,
        studentInfo: {
          _id: student._id,
          name: student.name,
          email: student.email,
          rollNo: student.rollNo,
          isDefaulter: true
        },
        classInfo: formattedClassInfo,
        sectionInfo: formattedSectionInfo,
        overdueCount: item.overdueCount,
        totalRemaining: item.totalRemaining,
        totalPendingAmount: pendingInfo.totalPendingAmount,
        totalPendingCount: pendingInfo.totalPendingCount,
        months: item.overdueFees.map(f => ({
          month: f.month,
          remaining: f.remainingAmount,
          originalAmount: f.originalAmount,
          finalAmount: f.finalAmount,
          dueDate: f.dueDate,
          daysOverdue: f.daysOverdue
        }))
      };
    }));

    return res.status(200).json({
      total,
      page,
      limit,
      totalPages: Math.ceil(total / limit),
      defaulters: data
    });
  } catch (err) {
    console.error("Error fetching defaulters:", err);
    res.status(500).json({ message: err.message });
  }
};

